"""
Model Card Generator
Reads eval_report.json + config.yaml and writes MODEL_CARD.md
following the Hugging Face model card spec (https://huggingface.co/docs/hub/model-card-annotated).
"""

from __future__ import annotations

import json
import sys
from datetime import date
from pathlib import Path

import yaml


TEMPLATE = """\
---
language: {language}
license: {license}
tags: {tags_yaml}
datasets:
  - {dataset}
metrics:
  - wer
  - cer
model-index:
  - name: {model_name}
    results:
      - task:
          type: automatic-speech-recognition
        dataset:
          name: {dataset}
          type: {dataset}
          split: test
        metrics:
          - type: wer
            value: {wer}
            name: Test WER
          - type: cer
            value: {cer}
            name: Test CER
---

# {model_name}

> Auto-generated on {date}. Maintainer: update with real author and provenance.

## Model Description

Fine-tuned [Wav2Vec2](https://huggingface.co/facebook/wav2vec2-base-960h) for Automatic Speech Recognition
on English speech. Based on `{base_model}`, trained with CTC objective.

- **Architecture:** Wav2Vec2 + CTC head
- **Base model:** `{base_model}`
- **Language:** {language}
- **License:** {license}
- **Training data:** {dataset} ({dataset_config})

## Intended Uses & Limitations

**Intended uses:**
{intended_uses}

**Out-of-scope:**
{out_of_scope}

**Limitations:**
{limitations}

## Training Details

| Hyperparameter | Value |
|---|---|
| Learning rate | `{lr}` |
| Batch size | `{batch_size}` |
| Epochs | `{epochs}` |
| Warmup steps | `{warmup_steps}` |
| FP16 | `{fp16}` |
| Seed | `{seed}` |

## Evaluation Results

Evaluated on the **held-out test split** (no test data was seen during training or hyperparameter tuning).

| Metric | Value |
|---|---|
| **WER** | **{wer_pct}%** |
| **CER** | **{cer_pct}%** |
| RTF (p95) | {rtf} |
| p95 Latency | {p95_lat} ms |
| Latency SLA ({lat_sla} ms) | {lat_sla_pass} |
| RTF SLA (<{rtf_thresh}) | {rtf_sla_pass} |

### WER Distribution (per-sample)

| Percentile | WER |
|---|---|
| p25 | {p25_wer} |
| p50 (median) | {p50_wer} |
| p75 | {p75_wer} |
| p95 | {p95_wer} |

## Bias Evaluation

WER was computed separately for each demographic subgroup below.
A gap > 10 percentage points between best and worst subgroup triggers a BIAS ALERT.

{bias_section}

> **Ethical considerations:** {ethical}

## Usage

```python
from transformers import Wav2Vec2ForCTC, Wav2Vec2Processor
import torch, librosa

model_dir = "{model_name}"
processor = Wav2Vec2Processor.from_pretrained(model_dir)
model = Wav2Vec2ForCTC.from_pretrained(model_dir)

audio, sr = librosa.load("speech.wav", sr=16000)
inputs = processor(audio, sampling_rate=sr, return_tensors="pt", padding=True)
with torch.no_grad():
    logits = model(**inputs).logits
pred_ids = torch.argmax(logits, dim=-1)
print(processor.decode(pred_ids[0]))
```

## Citation

```bibtex
@misc{{{model_name_safe},
  title     = {{{model_name}}},
  year      = {{{year}}},
  note      = {{Generated by SAGE ASR pipeline}}
}}
```
"""


def _bias_table(bias_report: dict) -> str:
    lines = []
    for group, breakdown in bias_report.items():
        lines.append(f"\n### {group}\n")
        lines.append("| Subgroup | WER |")
        lines.append("|---|---|")
        for subval, wer_val in breakdown.items():
            flag = " ⚠️" if wer_val > 0.20 else ""
            lines.append(f"| {subval} | {wer_val:.4f}{flag} |")
    return "\n".join(lines) if lines else "_No demographic metadata available in test set._"


def generate(config_path: str = "config.yaml", eval_report_path: str | None = None) -> str:
    with open(config_path) as f:
        cfg = yaml.safe_load(f)

    output_dir = Path(cfg["experiment"]["output_dir"])
    eval_report_path = eval_report_path or str(output_dir / "eval_report.json")

    with open(eval_report_path) as f:
        report = json.load(f)

    mc_cfg = cfg["model_card"]
    m = report["metrics"]
    sla = report["sla"]
    pct = report.get("percentile_wer", {})

    card = TEMPLATE.format(
        language=mc_cfg["language"],
        license=mc_cfg["license"],
        tags_yaml="\n  - ".join([""] + mc_cfg["tags"]),
        dataset=cfg["data"]["dataset_name"],
        dataset_config=cfg["data"]["dataset_config"],
        model_name=cfg["experiment"]["name"],
        model_name_safe=cfg["experiment"]["name"].replace("-", "_"),
        base_model=cfg["model"]["base_model"],
        date=str(date.today()),
        year=date.today().year,
        intended_uses=mc_cfg["intended_uses"],
        out_of_scope=mc_cfg["out_of_scope"],
        limitations=mc_cfg["limitations"],
        ethical=mc_cfg["ethical_considerations"],
        # Training params
        lr=cfg["training"]["learning_rate"],
        batch_size=cfg["training"]["per_device_train_batch_size"],
        epochs=cfg["training"]["num_epochs"],
        warmup_steps=cfg["training"]["warmup_steps"],
        fp16=cfg["training"]["fp16"],
        seed=cfg["experiment"]["seed"],
        # Metrics
        wer=m["wer"],
        cer=m["cer"],
        wer_pct=round(m["wer"] * 100, 2),
        cer_pct=round(m["cer"] * 100, 2),
        rtf=m.get("rtf", "N/A"),
        p95_lat=m.get("p95_latency_ms", "N/A"),
        lat_sla=cfg["inference"]["latency_sla_ms"],
        lat_sla_pass="✅ PASS" if sla.get("latency_pass") else "❌ FAIL",
        rtf_thresh=cfg["evaluation"]["rtf_sla_threshold"],
        rtf_sla_pass="✅ PASS" if sla.get("rtf_pass") else "❌ FAIL",
        # Percentiles
        p25_wer=pct.get("p25", "N/A"),
        p50_wer=pct.get("p50", "N/A"),
        p75_wer=pct.get("p75", "N/A"),
        p95_wer=pct.get("p95", "N/A"),
        # Bias
        bias_section=_bias_table(report.get("bias_by_group", {})),
    )

    out_path = output_dir / "MODEL_CARD.md"
    with open(out_path, "w") as f:
        f.write(card)

    print(f"Model card written: {out_path}")
    return str(out_path)


if __name__ == "__main__":
    import argparse

    parser = argparse.ArgumentParser(description="Generate ASR model card")
    parser.add_argument("--config", default="config.yaml")
    parser.add_argument("--eval_report", default=None)
    args = parser.parse_args()
    generate(args.config, args.eval_report)
