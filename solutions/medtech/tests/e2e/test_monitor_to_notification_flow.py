"""
SAGE[ai] - End-to-End tests for Monitor → Notification flow

Tests that events from Teams, Metabase, and GitLab correctly trigger
agent actions and audit log entries.
"""

import json
import sqlite3
from unittest.mock import MagicMock, patch

import pytest


pytestmark = pytest.mark.e2e

FIXED_ANALYSIS = {
    "severity": "HIGH",
    "root_cause_hypothesis": "test hypothesis",
    "recommended_action": "test action",
    "trace_id": "aaaaaaaa-bbbb-4ccc-8ddd-eeeeeeeeeeee",
}

MR_DRAFT_JSON = json.dumps({
    "mr_title": "Fix: sage-ai issue",
    "mr_description": "Resolves #1\n\nAuto-generated by SAGE[ai].",
})


def _query_audit(db_path, action_type):
    conn = sqlite3.connect(db_path)
    rows = conn.execute(
        "SELECT * FROM compliance_audit_log WHERE action_type = ?", (action_type,)
    ).fetchall()
    conn.close()
    return rows


